#ifndef TIPOS_H /* corresponde ao ficheiro tipos.h */
#define TIPOS_H

/*
 * typedef que mascar um char** como uma LISTA_STRING
 */
typedef char** LISTA_STRING;

/*
 * typedef que mascar um char* como uma STRING
 */
typedef char* STRING;

/*
 * typedef que mascar um int* como uma LISTA_INT
 */
typedef int* LISTA_INT;

/*
 * typedef que mascar um float* como uma LISTA_FLOAT
 */
typedef float* LISTA_FLOAT;

/*
 * typedef que mascar um int como uma BOOLEAN
 */
typedef int BOOLEAN;

#endif